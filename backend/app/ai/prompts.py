# =============================================================================
# 变量替换工具函数
# =============================================================================

def format_prompt(template: str, **kwargs: Any) -> str:
    """使用变量替换格式化提示词模板。

    支持两种占位符格式：
    1. {变量名} —— 标准 Python format 风格
    2. {{变量名}} —— 转义花括号

    对于未提供的变量，保留原始占位符不做替换。

    Args:
        template: 提示词模板字符串。
        **kwargs: 要替换的变量键值对。

    Returns:
        替换后的提示词字符串。

    Example:
        >>> prompt = format_prompt(
        ...     PRIVATE_CHAT_PROMPT,
        ...     user_name="张三",
        ...     current_time="2025-01-15 14:30:00",
        ...     chat_context="无历史对话",
        ... )
    """
    # 安全的 format：只替换提供的变量，未提供的保留原样
    class SafeDict(dict):
        def __missing__(self, key: str) -> str:
            return "{" + key + "}"

    # 先将双花括号转义，再替换单花括号
    safe_kwargs = SafeDict(kwargs)
    result = template

    # 使用安全的格式化
    try:
        result = result.format_map(safe_kwargs)
    except (KeyError, ValueError, IndexError):
        # 如果 format_map 仍有问题，使用正则替换
        for key, value in kwargs.items():
            result = result.replace("{" + key + "}", str(value))

    return result


def get_prompt_for_context(
    is_group: bool = False,
    **kwargs: Any,
) -> str:
    """根据对话上下文选择并格式化合适的提示词。

    私聊使用 PRIVATE_CHAT_PROMPT，群聊使用 GROUP_CHAT_PROMPT。

    Args:
        is_group: 是否为群聊。
        **kwargs: 提示词变量。

    Returns:
        格式化后的提示词字符串。
    """
    if is_group:
        template = GROUP_CHAT_PROMPT
    else:
        template = PRIVATE_CHAT_PROMPT
    return format_prompt(template, **kwargs)
